package com.stary.musicgo;

public class SearchList {
    private String name;
    private String aut;
    private String url;

    public String getAut() {
        return aut;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAut(String aut) {
        this.aut = aut;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
